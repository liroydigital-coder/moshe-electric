// Main: Generate 2 blog posts
// ========================================
function main() {
    // Load existing index
    let existingPosts = [];
    if (fs.existsSync(INDEX_FILE)) {
        existingPosts = JSON.parse(fs.readFileSync(INDEX_FILE, 'utf-8'));
    }

    // Find topics not yet published
    const publishedSlugs = new Set(existingPosts.map(p => p.slug));
    const available = BLOG_TOPICS.filter(t => !publishedSlugs.has(t.slug));

    if (available.length === 0) {
        console.log('All blog topics have been published! Add more topics to the BLOG_TOPICS array.');
        return;
    }

    // Pick 2 (or less if not enough available)
    const toPublish = available.slice(0, 2);
    const today = new Date();
    const dateStr = today.toISOString().split('T')[0];
    const hebrewDate = `${today.getDate().toString().padStart(2, '0')}/${(today.getMonth() + 1).toString().padStart(2, '0')}/${today.getFullYear()}`;

    for (const topic of toPublish) {
        const html = generatePostHTML(topic, hebrewDate);
        const filePath = path.join(POSTS_DIR, `${topic.slug}.html`);
        fs.writeFileSync(filePath, html, 'utf-8');

        existingPosts.unshift({
            title: topic.title,
            slug: topic.slug,
            excerpt: topic.excerpt,
            date: hebrewDate,
            dateISO: dateStr,
            keywords: topic.keywords
        });

        console.log(`Published: ${topic.title}`);
    }

    // Save updated index
    fs.writeFileSync(INDEX_FILE, JSON.stringify(existingPosts, null, 2), 'utf-8');
    console.log(`\nTotal posts: ${existingPosts.length}`);
    console.log('Index updated: blog/posts/index.json');
}

main();
